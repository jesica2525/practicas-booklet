let numero = parseInt(prompt("Ingrese un numero entre 1 y 4"));

if (numero == 1){
    alert("Elegiste a Homero");
} else if (numero == 2) {
    alert("Elegiste a Marge");
} else if (numero == 3) {
    alert("Elegiste a Bart");
} else if (numero == 4) {
    alert("Elegiste a Lisa");
}else {
    alert ("Tenias que elegir un numero entre 1 y 4");
}